# Numeric types
num1 = 10
num2 = 3.5

print("Addition:", num1 + num2)
print("Division:", num1 / 2)

# String types
name = "Claudia"
course = "Data Science"

print("Student:", name)
print("Course:", course)

# Mixing types safely
age = 21
print("Age:", str(age))

# Converting string to number
number_text = "100"
converted_number = int(number_text)
print("Converted:", converted_number + 50)

# Checking types
print(type(num1))
print(type(num2))
print(type(name))
print(type(converted_number))